                <p>Beryllium is a chemical element with symbol Be and atomic number 4. It is a relatively rare element in the universe, usually occurring as a product of the spallation of larger atomic nuclei that have collided with cosmic rays. Within the cores of stars beryllium is depleted as it is fused and creates larger elements.</p> 
                <p>It is a divalent element which occurs naturally only in combination with other elements in minerals. Notable gemstones which contain beryllium include beryl (aquamarine, emerald) and chrysoberyl. As a free element it is a steel-gray, strong, lightweight and brittle alkaline earth metal.</p>
                <p>Beryllium improves many physical properties when added as an alloying element to aluminium, copper (notably the alloy beryllium copper), iron and nickel. Beryllium does not form oxides until it reaches very high temperatures. Tools made of beryllium copper alloys are strong and hard and do not create sparks when they strike a steel surface. In structural applications, the combination of high flexural rigidity, thermal stability, thermal conductivity and low density (1.85 times that of water) make beryllium metal a desirable aerospace material for aircraft components, missiles, spacecraft, and satellites.</p> 
