                <p>Lead (/lɛd/) is a chemical element with atomic number 82 and symbol Pb (from Latin: plumbum). It is a soft, malleable, and heavy metal. Freshly cut solid lead has a bluish-white color that soon tarnishes to a dull grayish color when exposed to air; the liquid metal has shiny chrome-silver luster. Lead's density of 11.34 g/cm3 exceeds that of most common materials. Lead has the second highest atomic number of all practically stable elements. As such, lead is located at the end of some decay chains of heavier elements, which in part accounts for the relative abundance of lead: it exceeds those of other similarly-numbered elements.</p>
                <p>Lead is a post-transition metal, and is relatively inert unless powdered. Its weakened metallic character is illustrated by its general amphoteric nature: it and its oxides react with both acids and bases. It also displays a marked tendency toward covalent bonding. Its compounds are most commonly found in the +2 oxidation state, rather than +4, unlike the lighter group 14 elements.</p>
