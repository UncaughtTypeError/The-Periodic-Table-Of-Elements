                <p>Tennessine is a superheavy artificial chemical element with an atomic number of 117 and a symbol of Ts. Also known as eka-astatine or element 117, it is the second-heaviest known element and penultimate element of the 7th period of the periodic table. As of 2016, fifteen tennessine atoms have been observed: six when it was first synthesized in 2010, seven in 2012, and two in 2014.</p>
                <p>The discovery of tennessine was announced in Dubna, Russia, by a Russian–American collaboration in 2010, which makes it the most recently discovered element as of 2016. One of its daughter isotopes was created directly in 2011, partially confirming the results of the experiment. The experiment itself was repeated successfully by the same collaboration in 2012 and by a joint German–American team in 2014. In 2015, the Joint Working Party of the International Union of Pure and Applied Chemistry and the International Union of Pure and Applied Physics, which evaluates claims of discovery of new elements, has recognized the element and assigned the priority to the Russian–American team. In June 2016, the IUPAC published a declaration stating that the discoverers suggested the name tennessine after Tennessee, United States;[a] On 28 November 2016, the IUPAC officially adopted the name "tennessine".</p>
