                <p>Gadolinium is a chemical element with symbol Gd and atomic number 64. It is a silvery-white, malleable and ductile rare-earth metal. It is found in nature only in combined (salt) form. Gadolinium was first detected spectroscopically in 1880 by de Marignac who separated its oxide and is credited with its discovery. It is named for gadolinite, one of the minerals in which it was found, in turn named for chemist Johan Gadolin. The metal was isolated by Paul Emile Lecoq de Boisbaudran in 1886.</p>
                <p>Gadolinium metal possesses unusual metallurgic properties, to the extent that as little as 1% gadolinium can significantly improve the workability and resistance to high temperature oxidation of iron, chromium, and related alloys. Gadolinium as a metal or salt has exceptionally high absorption of neutrons and therefore is used for shielding in neutron radiography and in nuclear reactors. Like most rare earths, gadolinium forms trivalent ions with fluorescent properties. Gadolinium(III) salts have therefore been used as green phosphors in various applications.</p>
