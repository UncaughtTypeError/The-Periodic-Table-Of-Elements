                <p>Phosphorus is a chemical element with symbol P and atomic number 15. As an element, phosphorus exists in two major forms—white phosphorus and red phosphorus—but because it is highly reactive, phosphorus is never found as a free element on Earth. At 0.099%, phosphorus is the most abundant pnictogen in the Earth's crust. With few exceptions, minerals containing phosphorus are in the maximally oxidised state as inorganic phosphate rocks.</p>
                <p>The first form of elemental phosphorus to be produced (white phosphorus, in 1669) emits a faint glow when exposed to oxygen – hence the name, taken from Greek mythology, Φωσφόρος meaning "light-bearer" (Latin Lucifer), referring to the "Morning Star", the planet Venus (or Mercury). The term "phosphorescence", meaning glow after illumination, originally derives from this property of phosphorus, although this word has since been used for a different physical process that produces a glow. The glow of phosphorus itself originates from oxidation of the white (but not red) phosphorus — a process now termed chemiluminescence. Together with nitrogen, arsenic, and antimony, phosphorus is classified as a pnictogen.</p>
