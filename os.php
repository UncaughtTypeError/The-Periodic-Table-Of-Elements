                <p>Osmium (from Greek ὀσμή osme, "smell") is a chemical element with symbol Os and atomic number 76. It is a hard, brittle, bluish-white transition metal in the platinum group that is found as a trace element in alloys, mostly in platinum ores. Osmium is the densest naturally occurring element, with a density of 22.59 g/cm3. Its alloys with platinum, iridium, and other platinum-group metals are employed in fountain pen nibs, electrical contacts, and other applications where extreme durability and hardness are needed.</p>
                <p>Osmium has a blue-gray tint and is the densest stable element, slightly denser than iridium. Calculations of density from the X-ray diffraction data may produce the most reliable data for these elements, giving a value of 22.562±0.009 g/cm3 for iridium versus 22.587±0.009 g/cm3 for osmium.</p>
                <p>Osmium is a hard but brittle metal that remains lustrous even at high temperatures. It has a very low compressibility.</p>
