            <div class="card description">
                <p>Barium is a chemical element with symbol Ba and atomic number 56. It is the fifth element in Group 2, a soft silvery metallic alkaline earth metal. Because of its high chemical reactivity, barium is never found in nature as a free element. Its hydroxide, known in pre-modern history as baryta, does not occur as a mineral, but can be prepared by heating barium carbonate.</p>
                <p>The most common naturally occurring minerals of barium are barite (barium sulfate, BaSO4) and witherite (barium carbonate, BaCO3), both insoluble in water. The barium name originates from the alchemical derivative "baryta", from Greek βαρύς (barys), meaning "heavy." Baric is the adjective form of barium. Barium was identified as a new element in 1774, but not reduced to a metal until 1808 with the advent of electrolysis.</p>
                <p>Barium has few industrial applications. Historically, it was used as a getter for vacuum tubes.</p>
