                <p>Thulium is a chemical element with symbol Tm and atomic number 69. It is the thirteenth and antepenultimate (third-last) element in the lanthanide series. Like the other lanthanides, the most common oxidation state is +3, seen in its oxide, halides and other compounds. In aqueous solution, like compounds of other late lanthanides, soluble thulium compounds form complexes with nine water molecules.</p>
                <p>In 1879, Swedish chemist Per Teodor Cleve separated in the rare earth erbia another two previously unknown components, which he called holmia and thulia: these were the oxides of holmium and thulium respectively. A relatively pure sample of thulium metal was first obtained in 1911.</p>
                <p>Thulium is the second least abundant of the lanthanides after promethium, which is only found in trace quantities on Earth. It is an easily workable metal with a bright silvery-gray luster. It is fairly soft and slowly tarnishes in air. Despite its high price and rarity, thulium is used as the radiation source in portable X-ray devices and in solid-state lasers. It has no significant biological role and is not particularly toxic.</p>
