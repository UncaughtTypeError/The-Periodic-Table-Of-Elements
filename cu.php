                <p>Copper is a chemical element with symbol Cu (from Latin: cuprum) and atomic number 29. It is a soft, malleable and ductile metal with very high thermal and electrical conductivity. A freshly exposed surface of pure copper has a reddish-orange color. It is used as a conductor of heat and electricity, as a building material and as a constituent of various metal alloys, such as sterling silver used in jewelry, cupronickel used to make marine hardware and coins and constantan used in strain gauges and thermocouples for temperature measurement.</p>
                <p>Copper is found as a pure metal in nature, and this was the first source of the metal to be used by humans, c. 8000 BC. It was the first metal to be smelted from its ore, c. 5000 BC, the first metal to be cast into a shape in a mold, c. 4000 BC and the first metal to be purposefully alloyed with another metal, tin, to create bronze, c. 3,500 BC.</p>
                <p>In the Roman era, copper was principally mined on Cyprus, the origin of the name of the metal, from aes сyprium (metal of Cyprus), later corrupted to сuprum, from which the words copper (English), cuivre (French), Koper (Dutch) and Kupfer (German) are all derived.</p>
