                <p>Oganesson (symbol Og) is a transactinide chemical element. It was first synthesized in 2002 by a joint team of Russian and American scientists at the Joint Institute for Nuclear Research (JINR) in Dubna, Russia. With the atomic number 118 in the periodic table, the synthetic element is also known as element 118, or eka-radon. In December 2015, it was recognized as one of four new elements by the Joint Working Party of international scientific bodies IUPAC and IUPAP. It was formally named on 28 November 2016. The name is in line with the tradition of honoring a scientist and recognizes nuclear physicist Yuri Oganessian, who has played a leading role in the discovery of the heaviest elements in the periodic table. It is one of only two elements named after a living person at the time of naming, the other being seaborgium.</p>
                <p>Oganesson has the highest atomic number and highest atomic mass of all known elements. The radioactive oganesson atom is very unstable, and since 2005, only four atoms of the isotope 294Og have been detected.</p>
