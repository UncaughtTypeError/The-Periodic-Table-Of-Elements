                <p>Tungsten, also known as wolfram, is a chemical element with symbol W and atomic number 74. The word tungsten comes from the Swedish language tung sten, which directly translates to heavy stone. Its name in Swedish is volfram, however, in order to distinguish it from scheelite, which is alternatively named tungsten in Swedish.</p>
                <p>A hard, rare metal under standard conditions when uncombined, tungsten is found naturally on Earth almost exclusively in chemical compounds. It was identified as a new element in 1781, and first isolated as a metal in 1783. Its important ores include wolframite and scheelite. The free element is remarkable for its robustness, especially the fact that it has the highest melting point of all the elements. Its high density is 19.3 times that of water, comparable to that of uranium and gold, and much higher (about 1.7 times) than that of lead. Polycrystalline tungsten is an intrinsically brittle and hard material, making it difficult to work. However, pure single-crystalline tungsten is more ductile, and can be cut with a hard-steel hacksaw.</p>
