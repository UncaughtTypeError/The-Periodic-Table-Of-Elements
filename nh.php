            <div class="card description">
                <p>Nihonium (symbol Nh) is a chemical element with atomic number 113. It is a synthetic element (an element that can be created in a laboratory but is not found in nature) and is extremely radioactive; its most stable known isotope, nihonium-286, has a half-life of 20 seconds. It is also known as eka-thallium or simply element 113. Nihonium was first reported to have been created in 2003 by the Joint Institute for Nuclear Research in Dubna, Russia, and in 2004 by a team of Japanese scientists at RIKEN. In December 2015, the International Union of Pure and Applied Chemistry (IUPAC) and the International Union of Pure and Applied Physics (IUPAP) recognized the element and assigned the priority of the discovery to RIKEN. In November 2016, the IUPAC published a declaration defining the name to be nihonium. The name comes from the common Japanese name for Japan (日本 nihon?). On 28 November 2016, the name became official.</p>
                <p>In the periodic table, it is a p-block transactinide element. It is a member of the 7th period and is placed in the boron group, although it has not been confirmed to behave as the heavier homologue to thallium in the boron group.</p>
