            <div class="card description">
                <p>Iron is a chemical element with symbol Fe (from Latin: ferrum) and atomic number 26. It is a metal in the first transition series. It is by mass the most common element on Earth, forming much of Earth's outer and inner core. It is the fourth most common element in the Earth's crust. Its abundance in rocky planets like Earth is due to its abundant production by fusion in high-mass stars, where it is the last element to be produced with release of energy before the violent collapse of a supernova, which scatters the iron into space.</p>
                <p>Like the other group 8 elements, ruthenium and osmium, iron exists in a wide range of oxidation states, −2 to +6, although +2 and +3 are the most common. Elemental iron occurs in meteoroids and other low oxygen environments, but is reactive to oxygen and water. Fresh iron surfaces appear lustrous silvery-gray, but oxidize in normal air to give hydrated iron oxides, commonly known as rust. Unlike the metals that form passivating oxide layers, iron oxides occupy more volume than the metal and thus flake off, exposing fresh surfaces for corrosion.</p>
