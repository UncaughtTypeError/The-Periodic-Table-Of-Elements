                <p>Holmium is a chemical element with symbol Ho and atomic number 67. Part of the lanthanide series, holmium is a rare earth element. Holmium was discovered by Swedish chemist Per Theodor Cleve. Its oxide was first isolated from rare earth ores in 1878 and the element was named after the city of Stockholm.</p>
                <p>Elemental holmium is a relatively soft and malleable silvery-white metal. It is too reactive to be found uncombined in nature, but when isolated, is relatively stable in dry air at room temperature. However, it reacts with water and corrodes readily, and will also burn in air when heated.</p>
                <p>Holmium is found in the minerals monazite and gadolinite, and is usually commercially extracted from monazite using ion exchange techniques. Its compounds in nature, and in nearly all of its laboratory chemistry, are trivalently oxidized, containing Ho(III) ions. Trivalent holmium ions have fluorescent properties similar to many other rare earth ions (while yielding their own set of unique emission light lines), and holmium ions are thus used in the same way as some other rare earths in certain laser and glass colorant applications.</p>
