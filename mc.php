            <div class="card description">
                <p>Moscovium (element symbol: Mc) is a superheavy synthetic element that was first synthesized in 2003 by a joint team of Russian and American scientists at the Joint Institute for Nuclear Research (JINR) in Dubna, Russia. With the atomic number 115 in the periodic table, it is also known as element 115, or eka-bismuth. In December 2015, it was recognized as one of four new elements by the Joint Working Party of international scientific bodies IUPAC and IUPAP. On 28 November 2016, it was officially named.</p>
				<p>Moscovium is an extremely radioactive element: its most stable known isotope, moscovium-289, has a half-life of only 220 milliseconds.[8] In the periodic table, it is a p-block transactinide element. It is a member of the 7th period and is placed in group 15 as the heaviest pnictogen, although it has not been confirmed to behave as a heavier homologue of the pnictogen bismuth. Moscovium is calculated to have some properties similar to its lighter homologues, nitrogen, phosphorus, arsenic, antimony, and bismuth, and to be a post-transition metal, although it should also show several major differences from them. About 100 atoms of moscovium have been observed to date, all of which have been shown to have mass numbers from 287 to 290.</p>
