                <p>Cerium is a soft, ductile, silvery-white metallic chemical element with symbol Ce and atomic number 58. Tarnishing rapidly when exposed to air, it is soft enough to be cut with a knife. Cerium is the second element in the lanthanide series, and while it often shows the +3 state characteristic of the series, it also exceptionally has a stable +4 state that does not oxidise water. It is also traditionally considered to be one of the rare earth elements. Cerium has no biological role, and is not very toxic.</p>
                <p>Despite always being found in combination with the other rare earth elements in minerals such as monazite and bastnäsite, cerium is easy to extract from its ores, as it can be distinguished among the lanthanides by its unique ability to be oxidised to the +4 state. It is the most common of the lanthanides, followed by neodymium, lanthanum, and praseodymium. It is the 26th most abundant element, making up 66 ppm of the Earth's crust, half as much as chlorine and five times as much as lead.</p>
